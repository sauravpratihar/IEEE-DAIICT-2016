							<h4>Article I. General</h4>
1. The Executive Committee shall be selected by the nomination file by the members of the last year's Volunteer Team (as on Dec 31), preferably in Jan. The Executive Committee comprises of:<br>
a) Chairperson<br>
b) Vice Chairperson<br>
c) Secretary<br>
d) Treasurer<br>
e) Webmaster<br>
<br>
2. A Volunteer Team of 10-12 members shall be elected for which election shall be conducted as far as possible in Jan every year. This team will be subjected to expand twice in a year to include newly joined active members in the team (selected by current Executive Committee members).<br><br>
3. Term of each Executive Committee and the Volunteer Team shall be at least one calendar year.<br>
<br><br>
<h4>Article II. Qualifications for being selected to an executive officer position</h4>

All prospective executive officers must be an IEEE Student member.<br>
All prospective executive officers must be a member of the last Volunteer Team as on Dec 31.<br>
All prospective executive officers must be in good academic standing with the Institute (at least 6.0/10.0 CPI).<br>
All prospective executive officers from B-Tech course or Post Graduation course must have completed a minimum of three or one full semester (autumn or winter) at the Institute, respectively.<br>
Students of final semester are not eligible for nominations for aforementioned executive officer positions.<br>
<br><br>
<h4>Article III. Qualifications for being elected to a Volunteer Team member</h4>

1. All prospective Volunteer Team members must be an IEEE Student member.<br>
2. All prospective Volunteer Team members must be in good academic standing with the Institute (at least 6.0/10.0 CPI).<br>
3. All prospective Volunteer Team members from B-Tech course or Post Graduation course must have completed a minimum of three or one full semester (autumn or winter) at the Institute, respectively.<br>
4. Students of final semester are not eligible for nominations for Volunteer Team member.<br>
5. A person can file nomination for only one post at a time.<br>
<br><br>
<h4>Article IV. Qualifications for being selected to a Volunteer Team member (Additionally to all the Requirements stated in Article III.)</h4>

1. All prospective Volunteer Team members must have recently volunteered in at least two activities of the IEEE Student Branch, DA-IICT.<br>
<br><br>
<h4>Article V. Conduction of Volunteer Team member's Elections</h4>

At least one member of the last executive committee, who is not contesting for any post, shall preside at the Volunteer Team member's elections, in the absence of fulfillment of this condition the branch counselor shall preside at the elections.
10 nominees with maximum votes will be declared as the member of the Volunteer Team.
In case of tie up Branch counselor will have the final say.<br><br>
<br>
<h4>Article VI. Conduction of Executive Committee officer's selection</h4>

The selection of the officers will be on the basis of the individual credit earned.<br>
The order of the selection will be Chair, Vice Chair, Secretary and Treasurer.<br>
In a general branch meeting all the members of the last Executive Committee and the Branch Counselor will give certain credits (on the scale of 10) to each competitor for each post.<br>
Nominee with maximum credit will be declared winner for that particular post.<br>
In case no one files nomination for any post/s then the Branch Counselor and the last Executive Committee will nominate a member/s from the last Volunteer Team (as on Dec 31) for that post/s.<br>
In case of tie up Branch counselor will have the final say.<br>
<br><br>
<h4>Article VII. Expansion of the Volunteer Team (twice a year)</h4>

1. Every twice in a year(probably in April and November) nomination will be invited from the IEEE Student Branch member who are not the member of Volunteer Team.<br>
2. In a general branch meeting all the members of the Volunteer Team, Executive Committee and Branch Counselor will give certain credits(on the scale of 10) to each nominee.<br>
3. Nominee crossing the threshold of 6*total number of person giving credit will be declared as the new member of the Volunteer Team.<br>
4. In case no one able to cross the threshold then no expansion will be done.<br>
<br><br>
<h4>Article VIII. Nomination for additional positions</h4>

1. The following positions would be filled at discretion of the branch counselor.<br>
a. Batch representative. (If IEEE members from a respective batch are more than 10, the branch counselor, the current Executive Committee and the current Volunteer Team would select the batch representative).<br>
<br><br>
<h4>Article IX. Inactivity/Expulsion from Volunteer team and Executive committee.</h4>

1. The inactivity of a Volunteer/officer is defined as:<br>
a. Not attending the branch meetings & Showing disinterest in the branch affairs.<br>
b. Not performing the aforementioned duties.<br>
c. Not abiding by the constitution of the Team/Committee.<br>
d. Any kind of threatening behavior using the position of office-holder.<br>
<br>
2. Any Volunteer/officer is subject to expulsion by the Team/Committee on grounds of the aforementioned reasons.<br>
3. Expulsion may be initiated by any Team member or officer, by mailing to the Counselor.<br>
4. The Counselor will have to call an urgent branch meeting with in 3 working days of receiving such mail.<br>
5. The process of expulsion shall be carried out in the branch meeting. The member shall be expelled by a 2/3rd majority in the Team + Committee members (present in the meeting), with consent of Counselor.<br>
<br><br>
<h4>Article X. Officer Transition procedures</h4>

All executive officers shall maintain a notebook recording their experiences for the academic year. This notebook should contain a written job description and a dynamic "best practices" document which records past program successes and failures.<br>
The secretary shall be responsible for maintaining a branch notebook, which must contain copies of the following documents:<br>

Annual plan<br>
Annual report<br>
Officer meeting minutes<br>
Attendance rosters for faculty presentations and general meetings.<br>
Counselor shall convene a general branch meeting within a week of announcement of the results for the elections and selections. The agenda of the meeting would be handing over of responsibilities to the newly selected executive committee and elected Volunteer member.<br>
Newly selected officers and elected Volunteer Team member shall take over office after this meeting.<br>
Outgoing officers shall then hand over their notebooks to the incoming officers.<br>
Chairman and Treasurer of the DA-IICT Student Branch shall be one of the authorized signatories along with the Branch Counselor for all financial matters and transactions.<br>
No product/service shall be hired/purchased on cash unless approved by above two of three authorized signatories.<br>
Chairman of branch shall be responsible for preparing Annual Plan for the branch. Draft of annual plan shall be prepared by September month for the coming year.<br><br><br>
