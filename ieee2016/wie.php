										WIE is largest international professional group dedicated to promoting women engineers and scientist. The idea is to provide the young women with a platform, encouragement and inspiration to come up with confidence and excellence in the technical career of Engineering.WIE is an effort to motivate the women to be a part of science, Technology and Engineering by bringing all men and women with such ambitions to put a joint effort towards this.<br><br>


IEEE aims to achieve this by taking following steps:
Recognizing their achievements in electrical and electronics engineering through IEEE awards. Promoting WIE at major technical conferences. We have also taken a few initiatives like discounts in membership for girls in order to increase the number of participants.
Advocate women in leadership roles in IEEE governance, providing assistance and support to the activities of these groups. Facilitating programs and activities for promoting girls' participation. We are planning to organize an interactive session by two of our seniors who were selected in Google, women of India contest.<br><br>
Administering the STAR program to mentor young women in junior and high schools. IEEE student branch is looking forward to deliver few lectures to the school students of Gandhinagar and Ahmadabad in order to inspire girls to take up engineering as a career.
