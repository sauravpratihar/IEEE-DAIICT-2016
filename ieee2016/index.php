		<h1 style="font-size: 50px; font-style: bold;color: #6699CC">Why Join IEEE?</h1>
		<h3>The Institute of Electrical and Electronics Engineers (IEEE)</h3>
		<p style="font-size:28px; font-style: italic;">IEEE appeared from the union of two engineering societies in the early sixties, the IRE (Institute of Radio Engineers) and the AIEE (American Institute of Electrical Engineers). The IEEE is a society for Electrical and Computer Engineers, but that also welcomes Computer Scientists and Physicists.

IEEE is what is known as a technical professional society. Every engineering branch has a technical professional society, Civil Engineering has ASCE (American Society of Civil Engineers), Mechanical Engineering has ASME (American Society of Mechanical Engineers).

What distinguishes IEEE from this other societies is that IEEE is international. There are IEEE chapters in every continent, and in almost every country, and in those countries where IEEE is not established it has links with the local engineering societies, such is the case with IKE (Institution of Electrical Engineers) in the United Kingdom of Great Britain and Northern Ireland. This makes IEEE the largest engineering society in the world.</p>

